        /// <summary>
        /// Check if the email is from a temporary email domain
        /// </summary>
        /// <param name="email"></param>
        /// <returns></returns>
        public static bool IsTemporaryEmail(string email)
        {
            if (email.IsNullOrEmpty()) return false;
            var args = email.Split('@');
            if (args.Length < 2) return false;
            var domain = args[1].ToLower();
            var isBlacklisted = TemporaryEmailDomains.Contains(domain);
            if (!isBlacklisted)
            {
                // check for patter ^\d+@
                // cover the case for spammers from China
                var isMatch = email.IsMatch(@"^\d+@", RegexOptions.Singleline);
                return isMatch;
            }

            return true;
        }

